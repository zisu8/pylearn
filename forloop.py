"""for loop example"""

# for loop example 1

print('\n' + "erstes for example mit einer sequence von 0 - 5" + "\n")

for x in (0, 1, 2, 3, 4, 5):
    print(x)

print('\n' + "zweites for example mit range(0,10)" + "\n")

